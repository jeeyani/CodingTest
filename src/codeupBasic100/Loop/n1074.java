package codeupBasic100.Loop;

import java.util.*;

public class n1074 {
	public static Scanner sc = new Scanner(System.in);
	
	public static void main(String[] args) {
		
		int n = 0;
		n = sc.nextInt();
		
		while (true) {
			System.out.println(n);
			n--;
			
			if(n ==0) break;

		}
		
	}
	
	
}
	