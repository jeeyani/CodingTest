package codeupBasic100.Loop;

import java.util.*;

public class n1073 {
	public static Scanner sc = new Scanner(System.in);
	
	public static void main(String[] args) {
		
		int n = 0;
		
		while (true) {
			n = sc.nextInt();
			if(n ==0) break;
			
			System.out.println(n);
		}
		
	}
	
	
}
	