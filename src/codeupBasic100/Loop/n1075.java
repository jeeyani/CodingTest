package codeupBasic100.Loop;

import java.util.*;

public class n1075 {
	public static Scanner sc = new Scanner(System.in);
	
	public static void main(String[] args) {
		
		int n = 0;
		n = sc.nextInt();
		
		while (true) {
			
			n--;
			System.out.println(n);
			if(n ==0) break;

		}
		
	}
	
	
}
	