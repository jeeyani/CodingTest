import java.util.*;

public class n1083 {
	public static Scanner sc = new Scanner(System.in);
	
	public static void main(String[] args) {
		
		int n = sc.nextInt();
		
		for (int i = 1; i <=n ; i++) {

			if(i%3==0) {
				System.out.print("X ");
			}
			
			else{System.out.print(i+" ");}
		}
		

		
	}
	
	
}
	